namespace DigitalStore.Enums
{
    // O enum 'PerfilEnum' representa os diferentes tipos de perfil de usuário na aplicação.
    // Cada valor no enum está associado a um número inteiro, que facilita a distinção entre os perfis de usuários.
    public enum PerfilEnum
    {
        // O estado 'Admin' representa um perfil de administrador, que tem permissões administrativas no sistema.
        Admin = 0,

        // O estado 'Cliente' representa um perfil de cliente, que pode fazer compras e interagir com a loja.
        Cliente = 1
    }
}
