namespace DigitalStore.Enums
{
    // Enumeração que representa os gêneros disponíveis no sistema.
    // Esse tipo de enumeração é útil para garantir que apenas valores predefinidos sejam utilizados no código.

    public enum GeneroEnum
    {
        // Representa o gênero feminino.
        Feminino = 0,

        // Representa o gênero masculino.
        Masculino = 1,

        // Representa outro gênero não especificado.
        Outro = 2
    }
}
