namespace DigitalStore.Enums
{
    // Definindo um enum chamado 'MetodoPagamentoEnum', que representa os métodos de pagamento possíveis
    // na aplicação. O valor associado a cada item será o inteiro indicado após o sinal de igual.
    public enum MetodoPagamentoEnum
    {
        // O método Cartão de crédito/débito é representado pelo valor 0.
        Cartao = 0,

        // O método Boleto é representado pelo valor 1.
        Boleto = 1,

        // O método Pix é representado pelo valor 2.
        Pix = 2
    }
}
